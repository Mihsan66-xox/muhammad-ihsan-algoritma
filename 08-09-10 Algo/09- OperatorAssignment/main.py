# Operasi yang dapat dilakukan dengan menringkas

a = 10 # adalah assignmen
print("nilai a, = ", a)
a += 10 # artinnya a = a + 1
print("nilai a, = ", a)
a -= 10 # artinya a = a - 1
print("nilai a, = ", a)
a *= 10 # artinya a = a * 1
print("nilai a, = ", a)
a /= 10 # artinya a = a / 1
print("nilai a, = ", a)

# Modulus dan floor division
b = 10
print("nilai b,  $= 3, nilai b menjadi ", b)
b %= 3 # artinya b = b % 3
b = 10
print("nilai b, //= 10, nilai b menjadi", b)
b //= 10 # artinya b = b // 3

# pangkat (ekponen)
a = 5
print("nilai a = ", a)
a **= 3
print("nilai a, **= 3, nilai a menjadi", a)

# OR (|)
c = True
print("nilai c |= false, nilai c menjadi ", c)
c |= False # artinya c = c or False
c = False
print("nilai c |= True, nilai c menjadi ", c)
c |= False # artinya c = c or False
c = False
print("nilai c |= false, nilai c menjadi ", c)
c |= False # artinya c = c or False

# AND (&)
c = False
print("nilai c &= True, nilai c menjadi ", c)
c &= False # artinya c = c and True
c = False
print("nilai c &= False, nilai c menjadi ", c)
c &= False # artinya c = c and True
c = True
print("nilai c &= True, nilai c menjadi ", c)
c &= False # artinya c = c and True